public class MathOperations {
    static void main(String[] args) {

        int x, y, z;
        x = 100;
        y = 5;
        x += 50; // x = x + 50
        z = x + y;
        z++; // z = z + 1

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);
        System.out.println(z);

        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x == y);
        System.out.println(x != y);

        /*
        && = and
        || = or
        ! = not
         */

    }
}
